class TestBlobs:
    """Tests for prebuilt libraries (vendor blobs) linked into the final image.

    The used module links two fake blobs the two ways real HAL modules link
    theirs: as an imported library (hal_stm32 style) and as -l/-L linker
    flags (hal_espressif style). Both must show up in the SBOM.
    """

    @pytest.fixture(params=[FILE_BLOB_IMPORTED, FILE_BLOB_FLAG])
    def blob_file(self, request, zephyr_doc):
        """Parametrized fixture providing each blob's SPDX file entry."""
        f = find_file_by_name(zephyr_doc, request.param)
        assert f is not None, f"zephyr.spdx: {request.param} not found"
        return f

    def test_blob_in_used_module_package(self, zephyr_doc, blob_file):
        """Each blob must be attributed to the module that provides it."""
        pkg = find_package_by_name(zephyr_doc, PKG_USED_MODULE_SOURCES)
        assert pkg is not None, f"zephyr.spdx: '{PKG_USED_MODULE_SOURCES}' package not found"
        contained = {
            str(r.related_spdx_element_id)
            for r in get_relationships_for_element(
                zephyr_doc, pkg.spdx_id, RelationshipType.CONTAINS
            )
        }
        assert blob_file.spdx_id in contained, (
            f"zephyr.spdx: {PKG_USED_MODULE_SOURCES} does not CONTAIN {blob_file.name}"
        )

    def test_blob_checksums(self, blob_file):
        """Each blob's checksums must match its actual content."""
        expected_sha1 = hashlib.sha1(BLOB_CONTENT, usedforsecurity=False).hexdigest()
        checksums = {c.algorithm: c.value for c in blob_file.checksums}
        assert checksums.get(ChecksumAlgorithm.SHA1) == expected_sha1, (
            f"zephyr.spdx: {blob_file.name} SHA1 mismatch"
        )

    def test_blob_license_from_reuse_toml(self, blob_file):
        """Binary blobs cannot carry SPDX headers; REUSE.toml supplies their license."""
        licenses = file_license_ids(blob_file)
        assert LICENSE_BLOBS in licenses, (
            f"zephyr.spdx: {blob_file.name} license should be {LICENSE_BLOBS} "
            f"(from REUSE.toml), got {licenses}"
        )

    def test_blob_copyright_from_reuse_toml(self, blob_file):
        """Binary blobs cannot carry SPDX headers; REUSE.toml supplies their copyright."""
        assert COPYRIGHT_BLOBS in str(blob_file.copyright_text), (
            f"zephyr.spdx: {blob_file.name} copyright should come from REUSE.toml "
            f"('{COPYRIGHT_BLOBS}'), got '{blob_file.copyright_text}'"
        )

    def test_blob_metadata_comment(self, blob_file):
        """Each blob's file comment must carry the module.yml blob metadata."""
        comment = str(blob_file.comment or "")
        assert "module blob" in comment, (
            f"zephyr.spdx: {blob_file.name} comment should describe the blob, got '{comment}'"
        )
        assert BLOB_URL_PREFIX in comment, (
            f"zephyr.spdx: {blob_file.name} comment should carry the blob url, got '{comment}'"
        )
        assert BLOB_VERSION in comment, (
            f"zephyr.spdx: {blob_file.name} comment should carry the blob version, got '{comment}'"
        )

    def test_blobs_static_linked_into_image(self, build_doc, zephyr_doc):
        """The final image must have a STATIC_LINK relationship to each blob."""
        elf = find_file_by_name(build_doc, FILE_ZEPHYR_ELF)
        assert elf is not None, f"build.spdx: {FILE_ZEPHYR_ELF} not found"

        linked = {
            str(r.related_spdx_element_id)
            for r in get_relationships_for_element(
                build_doc, elf.spdx_id, RelationshipType.STATIC_LINK
            )
        }
        for blob_name in (FILE_BLOB_IMPORTED, FILE_BLOB_FLAG):
            blob = find_file_by_name(zephyr_doc, blob_name)
            assert blob is not None, f"zephyr.spdx: {blob_name} not found"
            # the blob file lives in zephyr.spdx, so the relationship target
            # is prefixed with the document ref
            assert any(ref.endswith(blob.spdx_id) for ref in linked), (
                f"build.spdx: {FILE_ZEPHYR_ELF} has no STATIC_LINK to {blob_name}, got {linked}"
            )
