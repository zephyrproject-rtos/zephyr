int to_ppm(const char *filename, const uint32_t *buf, size_t w, size_t h)
{
	FILE *f = fopen(filename, "w");

	if (!f) {
		return -1;
	}

	fprintf(f, "P3\n");
	fprintf(f, "# %s\n", filename);
	fprintf(f, "%lu %lu\n", w, h);
	fprintf(f, "255\n");

	for (size_t i = 0; i < (w * h); i++) {
		fprintf(f, "%3d ", (buf[i] & 0xFF0000) >> 16);
		fprintf(f, "%3d ", (buf[i] & 0x00FF00) >> 8);
		fprintf(f, "%3d\n", (buf[i] & 0x0000FF) >> 0);
	}

	fclose(f);

	return 0;
}

