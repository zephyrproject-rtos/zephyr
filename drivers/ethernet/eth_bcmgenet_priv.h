#ifndef ZEPHYR_DRIVERS_ETHERNET_ETH_BCMGENET_H_
#define ZEPHYR_DRIVERS_ETHERNET_ETH_BCMGENET_H_

#include <zephyr/sys/util.h>
#include "eth.h"

#define GENET_ADDR_LOW(x)  ((uint32_t)(POINTER_TO_UINT(x) & UINT32_MAX))
#define GENET_ADDR_HIGH(x) ((uint32_t)((uint64_t)POINTER_TO_UINT(x) >> 32))

#define GENET_PORT_CTRL          0x04
#define GENET_PORT_MODE_EXT_GPHY 3

#define GENET_RBUF_FLUSH_CTRL    0x08
#define GENET_EXT_RGMII_OOB_CTRL 0x8C
#define GENET_RGMII_LINK         BIT(4)
#define GENET_OOB_DISABLE        BIT(5)
#define GENET_RGMII_MODE_EN      BIT(6)
#define GENET_ID_MODE_DIS        BIT(16)
#define GENET_RGMII_CFG          (GENET_RGMII_LINK | GENET_RGMII_MODE_EN | GENET_ID_MODE_DIS)

#define GENET_RBUF_CTRL      0x0300
#define GENET_RBUF_SIZE_CTRL 0x3B4
#define GENET_RBUF_ALIGN_2B  BIT(1)

#define GENET_MAC_CMD       0x808
#define GENET_MAC_ADDR_LOW  0x80c
#define GENET_MAC_ADDR_HIGH 0x810
#define GENET_MAC_MTU       0x814
#define GENET_MAC_TX_FLUSH  0xB34

#define GENET_MAC_MIB_CTRL     0xD80
#define GENET_MAC_MIB_RST_RX   BIT(0)
#define GENET_MAC_MIB_RST_RUNT BIT(1)
#define GENET_MAC_MIB_RST_TX   BIT(2)

#define GENET_MAC_TX_EN         BIT(0)
#define GENET_MAC_RX_EN         BIT(1)
#define GENET_MAC_SPEED_MASK    GENMASK(3, 2)
#define GENET_MAC_HD_EN         BIT(10)
#define GENET_MAC_SW_RST        BIT(13)
#define GENET_MAC_LOOP_EN       BIT(15)
#define GENET_MAC_SPEED(x)      ((x) << 2)

#define GENET_MAC_SPEED_10M   0
#define GENET_MAC_SPEED_100M  1
#define GENET_MAC_SPEED_1000M 2

/* Buffer descriptors, same for Rx/Tx */
#define GENET_NB_RX_DESCS 256
#define GENET_NB_TX_DESCS 256

#define GENET_QUEUES     16
#define GENET_QUEUES_CFG (1 << GENET_QUEUES)

/* DMA rings size */
#define GENET_DMA_RING_SIZE  64
#define GENET_DMA_RINGS_SIZE (GENET_DMA_RING_SIZE * (GENET_QUEUES + 1))

/* DMA descriptor */
#define GENET_DMA_DESC_STATUS        0x00
#define GENET_DMA_DESC_ADDR_LOW      0x04
#define GENET_DMA_DESC_ADDR_HIGH     0x08
#define GENET_DMA_RING_BUF_SIZE      0x10
#define GENET_DMA_START_ADDR         0x14
#define GENET_DMA_END_ADDR           0x1c
#define GENET_DMA_MBUF_DONE_THRES    0x24
#define GENET_DMA_DESC_SIZE          12
#define GENET_DMA_DESC_SIZE_GET(x)   (((x) * (GENET_DMA_DESC_SIZE / 4)) - 1)
#define GENET_DMA_DESC_OFF_GET(i, o) (((i) * GENET_DMA_DESC_SIZE) + (o))

/* Tx/Rx DMA descriptor bits */
#define GENET_DMA_EN BIT(0)

#define GENET_DMA_MAX_BURST_LEN       BIT(3)
#define GENET_DMA_WRAP                BIT(12)
#define GENET_DMA_SOP                 BIT(13)
#define GENET_DMA_EOP                 BIT(14)
#define GENET_DMA_OWN                 BIT(15)
#define GENET_DMA_RING_BUF_LEN_SHIFT  16
#define GENET_DMA_RING_BUF_LEN_SET(x) ((x) << GENET_DMA_RING_BUF_LEN_SHIFT)
#define GENET_DMA_RING_BUF_LEN_GET(x) (((x) >> GENET_DMA_RING_BUF_LEN_SHIFT) & UINT16_MAX)
#define GENET_DMA_RING_SIZE_SET(x)    ((x) << GENET_DMA_RING_BUF_LEN_SHIFT)
#define GENET_DMA_RING_BUF_EN(x)      BIT((x) + 1)

#define GENET_RX_OFF                0x2000
#define GENET_RX_DMA_REG_OFF        (GENET_RX_OFF + (GENET_NB_RX_DESCS * GENET_DMA_DESC_SIZE))
#define GENET_RX_DMA_REG_BASE       (GENET_RX_DMA_REG_OFF + GENET_DMA_RINGS_SIZE)
#define GENET_RX_DMA_RING_CFG       (GENET_RX_DMA_REG_BASE + 0x00)
#define GENET_RX_DMA_CTRL           (GENET_RX_DMA_REG_BASE + 0x04)
#define GENET_RX_DMA_SCB_BURST_SIZE (GENET_RX_DMA_REG_BASE + 0x0c)

#define GENET_TX_OFF                0x4000
#define GENET_TX_DMA_REG_OFF        (GENET_TX_OFF + (GENET_NB_TX_DESCS * GENET_DMA_DESC_SIZE))
#define GENET_TX_DMA_REG_BASE       (GENET_TX_DMA_REG_OFF + GENET_DMA_RINGS_SIZE)
#define GENET_TX_DMA_RING_CFG       (GENET_TX_DMA_REG_BASE + 0x00)
#define GENET_TX_DMA_CTRL           (GENET_TX_DMA_REG_BASE + 0x04)
#define GENET_TX_DMA_SCB_BURST_SIZE (GENET_TX_DMA_REG_BASE + 0x0c)

/* Tx specific DMA descriptor bits */
#define GENET_DMA_TX_DO_CSUM       BIT(4)
#define GENET_DMA_TX_OW_CRC        BIT(5)
#define GENET_DMA_TX_APPEND_CRC    BIT(6)
#define GENET_DMA_TX_UNDERRUN      BIT(9)
#define GENET_DMA_TX_QTAG_SHIFT    7
#define GENET_DMA_TX_QTAG_SET(x)   ((x) << GENET_DMA_TX_QTAG_SHIFT)

#define GENET_TX_DMA_RING_REG_BASE    (GENET_TX_DMA_REG_OFF + (GENET_QUEUES * GENET_DMA_RING_SIZE))
#define GENET_TX_DMA_READ_PTR         (GENET_TX_DMA_RING_REG_BASE + 0x00)
#define GENET_TX_DMA_CIDX             (GENET_TX_DMA_RING_REG_BASE + 0x08)
#define GENET_TX_DMA_PIDX             (GENET_TX_DMA_RING_REG_BASE + 0x0c)
#define GENET_TX_DMA_RING_BUF_SIZE    (GENET_TX_DMA_RING_REG_BASE + 0x10)
#define GENET_TX_DMA_START_ADDR       (GENET_TX_DMA_RING_REG_BASE + 0x14)
#define GENET_TX_DMA_END_ADDR         (GENET_TX_DMA_RING_REG_BASE + 0x1c)
#define GENET_TX_DMA_MBUF_DONE_THRESH (GENET_TX_DMA_RING_REG_BASE + 0x24)
#define GENET_TX_DMA_FLOW_PERIOD      (GENET_TX_DMA_RING_REG_BASE + 0x28)
#define GENET_TX_DMA_WRITE_PTR        (GENET_TX_DMA_RING_REG_BASE + 0x2c)

#define GENET_RX_DMA_RING_REG_BASE    (GENET_RX_DMA_REG_OFF + (GENET_QUEUES * GENET_DMA_RING_SIZE))
#define GENET_RX_DMA_WRITE_PTR        (GENET_RX_DMA_RING_REG_BASE + 0x00)
#define GENET_RX_DMA_PIDX             (GENET_RX_DMA_RING_REG_BASE + 0x08)
#define GENET_RX_DMA_CIDX             (GENET_RX_DMA_RING_REG_BASE + 0x0c)
#define GENET_RX_DMA_RING_BUF_SIZE    (GENET_RX_DMA_RING_REG_BASE + 0x10)
#define GENET_RX_DMA_START_ADDR       (GENET_RX_DMA_RING_REG_BASE + 0x14)
#define GENET_RX_DMA_END_ADDR         (GENET_RX_DMA_RING_REG_BASE + 0x1c)
#define GENET_RX_DMA_MBUF_DONE_THRESH (GENET_RX_DMA_RING_REG_BASE + 0x24)
#define GENET_RX_DMA_XOR_THRESH       (GENET_RX_DMA_RING_REG_BASE + 0x28)
#define GENET_RX_DMA_READ_PTR         (GENET_RX_DMA_RING_REG_BASE + 0x2c)

#define GENET_DMA_FC_THRESH_LOW   5
#define GENET_DMA_FC_THRESH_HIGH  (GENET_NB_RX_DESCS >> 4)
#define GENET_DMA_FC_THRESH_VALUE ((GENET_DMA_FC_THRESH_LOW << 16) | GENET_DMA_FC_THRESH_HIGH)

#define GENET_TX_TIMEOUT_US     100000
#define GENET_MAX_FRAME_LEN     1536
#define GENET_BUF_SIZE          CONFIG_NET_BUF_DATA_SIZE

#define GENET_BUF_RX_COUNT \
	(GENET_NB_RX_DESCS + DIV_ROUND_UP(GENET_MAX_FRAME_LEN, GENET_BUF_SIZE))

#endif /* ZEPHYR_DRIVERS_ETHERNET_ETH_BCMGENET_H_ */
