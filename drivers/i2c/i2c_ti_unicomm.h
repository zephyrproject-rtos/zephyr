#ifndef ZEPHYR_DRIVERS_I2C_TI_UNICOMM_H_
#define ZEPHYR_DRIVERS_I2C_TI_UNICOMM_H_

/*
 * UNICOMM Register Offsets
 */

#define UNICOMM_POWER_EN  0x00000800U
#define UNICOMM_RESET_CTL 0x00000804U
#define UNICOMM_CLOCK_CFG 0x00000808U
#define UNICOMM_STATUS    0x00000814U
#define UNICOMM_MODE      0x00001100U

/*
 * UNICOMM I2CC Register Offsets
 */

#define UNICOMM_I2CC_CLKDIV          0x00000000U
#define UNICOMM_I2CC_CLKSEL          0x00000008U
/* SKIPPED - cpu int, dma trig0,1 */
#define UNICOMM_I2CC_INTCTL          0x000000E4U
#define UNICOMM_I2CC_CONTROL         0x00000100U
#define UNICOMM_I2CC_CONFIG          0x00000104U
#define UNICOMM_I2CC_STATUS          0x00000108U
#define UNICOMM_I2CC_FIFO_LEVEL      0x0000010CU
#define UNICOMM_I2CC_TPR             0x00000110U
/* SKIPPED - glitch filter control, bus monitor,  */
#define UNICOMM_I2CC_TXDATA          0x00000120U
#define UNICOMM_I2CC_RXDATA          0x00000124U
/* SKIPPED - PEC status */
#define UNICOMM_I2CC_TARGET_ADDRESS  0x0000014CU
#define UNICOMM_I2CC_TIMEOUT_COUNT   0x00000150U
#define UNICOMM_I2CC_TIMEOUT_CONTROL 0x00000154U
/* SKIPPED - PEC control */

/*
 * UNICOMM I2CT Register Offsets
 */

#define UNICOMM_I2CT_CLKDIV        UNICOMM_I2CC_CLKDIV
#define UNICOMM_I2CT_CLKSEL        UNICOMM_I2CC_CLKSEL
#define UNICOMM_I2CT_STATUS        UNICOMM_I2CC_STATUS
#define UNICOMM_I2CT_ACKCTL        0x00000104U
#define UNICOMM_I2CT_CPU_INT_IIDX  0x00000020U
#define UNICOMM_I2CT_CPU_INT_IMASK 0x00000028U
#define UNICOMM_I2CT_CPU_INT_ICLR  0x00000048U
/* SKIPPED - dma trig0,1 */
#define UNICOMM_I2CT_INTCTL        UNICOMM_I2CC_INTCTL
#define UNICOMM_I2CT_CONTROL       UNICOMM_I2CC_CONTROL
#define UNICOMM_I2CT_OAR           0x0000014CU
#define UNICOMM_I2CT_IFLS          0x0000010CU
#define UNICOMM_I2CT_TXDATA        0x00000120U
#define UNICOMM_I2CT_RXDATA        0x00000124U

/*
 * Peripheral Configuration Values
 */

#define PWREN_ENABLE      0x00000001U
#define PWREN_DISABLE     0x00000000U
#define PWREN_KEY         0x26000000U
#define IPMODE_CONTROLLER 0x00000002U
#define IPMODE_TARGET     0x00000003U

#define RESET_CTL_KEY_UNLOCK       0xB1000000U
#define RESET_CTL_STICKY_BIT_CLEAR 0x00000002U
#define RESET_CTL_ASSERT_RESET     0x00000001U

/*
 * I2CC Configuration Values
 */

#define I2CC_CLKSEL_BUSCLK_ENABLE 0x00000008U

#define I2CC_CONFIG_ENABLE            0x00000001U
#define I2CC_CONFIG_CLKSTRETCH_ENABLE 0x00000004U

#define I2CC_TARGET_ADDRESS_OFFSET             0x1
#define I2CC_TARGET_ADDRESS_DIRECTION_TRANSMIT 0x00000000U
#define I2CC_TARGET_ADDRESS_DIRECTION_RECEIVE  0x00000001U

#define I2CC_CONTROL_BLEN_OFFSET   0x00000010U
#define I2CC_CONTROL_START_ENABLE  0x00000002U
#define I2CC_CONTROL_START_DISABLE 0x00000000U
#define I2CC_CONTROL_STOP_ENABLE   0x00000004U
#define I2CC_CONTROL_STOP_DISABLE  0x00000000U
#define I2CC_CONTROL_ACK_ENABLE    0x00000008U
#define I2CC_CONTROL_ACK_DISABLE   0x00000000U
#define I2CC_CONTROL_START_FRAME   0x00000001U

#define I2CC_RX_FIFO_LEVEL_NOT_EMPTY 0x00000040U

/* SCL period = (1 + TPR) * (SCL_LP + SCL_HP) * CLK_PRD, where SCL_LP=6, SCL_HP=4 */
#define I2CC_SCL_LP_HP 10U

/*
 * I2CT Configuration Values
 */
#define I2CT_RX_FIFO_LEVEL_NOT_EMPTY 0x00000040U

#define I2CT_CONTROL_ENABLE              0x00000001U
#define I2CT_CONTROL_TXEMPTY_ON_TREQ     0x00000008U
#define I2CT_CONTROL_TXWAIT_STALE_TXFIFO 0x00000020U

#define I2CT_OAR_MODE_7BIT  0x00000000U
#define I2CT_OAR_MODE_10BIT 0x00008000U
#define I2CT_OAR_ENABLE     0x00004000U

/*
 * I2CT Interrupt Index Values (read from UNICOMM_I2CT_CPU_INT_IIDX)
 */

#define I2CT_IIDX_NO_INTR                 0x00000000U
#define I2CT_IIDX_TARGET_RX_DONE          0x00000001U
#define I2CT_IIDX_TARGET_TX_DONE          0x00000002U
#define I2CT_IIDX_TARGET_RXFIFO_TRIGGER   0x00000003U
#define I2CT_IIDX_TARGET_RXFIFO_FULL      0x00000005U
#define I2CT_IIDX_TARGET_TXFIFO_EMPTY     0x00000006U
#define I2CT_IIDX_TARGET_TXFIFO_UNDERFLOW 0x00000007U
#define I2CT_IIDX_TARGET_START            0x0000000AU
#define I2CT_IIDX_TARGET_STOP             0x0000000BU

/*
 * Masks
 */

#define UNICOMM_I2CC_STATUS_TXFIFOFULL_BIT 0x00004000U
#define UNICOMM_I2CC_STATUS_BUSY           0x00000001U
#define UNICOMM_I2CC_STATUS_ERR            0x00000002U /* NACK received */

#define UNICOMM_I2CC_FIFO_LEVEL_TXCLR 0x00000008U /* flush TX FIFO */

#define UNICOMM_I2CC_TARGET_ADDRESS_DIRECTION_MASK 0x00000001U
#define UNICOMM_I2CC_TARGET_ADDRESS_ADDR_MASK      0x000007FEU

#define UNICOMM_I2CC_CONTROL_BLEN_MASK        0x0FFF0000U
#define UNICOMM_I2CC_CONTROL_START_MASK       0x00000002U
#define UNICOMM_I2CC_CONTROL_STOP_MASK        0x00000004U
#define UNICOMM_I2CC_CONTROL_ACK_MASK         0x00000008U
#define UNICOMM_I2CC_CONTROL_FRAME_START_MASK 0x00000001U

#define I2CT_CONTROL_CLKSTRETCH_MASK 0x00100000U

#define I2CT_OAR_MODE_MASK   0x00008000U
#define I2CT_OAR_ADDR_MASK   0x000003FFU
#define I2CT_OAR_ENABLE_MASK 0x00000400U

#define I2CT_7BIT_ADDR_MASK  0x0000007F
#define I2CT_10BIT_ADDR_MASK 0x000003FF

#define I2CT_RX_FIFO_LEVEL_MASK 0x00000070U
#define I2CT_TX_FIFO_LEVEL_MASK 0x00000007U

#define I2CT_STATUS_RXFIFOEMPTY_BIT 0x00000800U

#define I2CT_IFLS_TXCLR_MASK 0x00000008U /* bit 3: set to flush TX FIFO */
#define I2CT_STATUS_TXFE_BIT 0x00002000U /* bit 13: TX FIFO empty */

#define I2CT_ACKCTL_ACKOEN_MASK    0x00000001U /* bit 0: ACK override enable */
#define I2CT_ACKCTL_ACKOVAL_MASK   0x00000002U /* bit 1: ACK / NACK setting */
#define I2CT_ACKCTL_ACKOEN_ENABLE  0x00000001U
#define I2CT_ACKCTL_ACKOEN_DISABLE 0x00000000U
#define I2CT_ACKCTL_ACKOVAL_MASK   0x00000002U /* bit 1: 0=ACK, 1=NACK */
#define I2CT_ACKCTL_ACKOVAL_NACK   0x00000002U
#define I2CT_ACKCTL_ACKOVAL_ACK    0x00000000U

/* Miscellaneous */
#define IMASK_ALL          0xFFFFFFFF

#define IMASK_I2CT_RXDONE  0x00000001U
#define IMASK_I2CT_TXDONE  0x00000002U
#define IMASK_I2CT_RXTRG   0x00000004U
#define IMASK_I2CT_TXTRG   0x00000008U
#define IMASK_I2CT_RXFULL  0x00000010U
#define IMASK_I2CT_TXEMPTY 0x00000020U
#define IMASK_I2CT_TXUNFL  0x00000040U
#define IMASK_I2CT_RXOVFL  0x00000080U
#define IMASK_I2CT_START   0x00000200U
#define IMASK_I2CT_STOP    0x00000400U

#define BYTE_MASK          0xFF
#define DIRECTION_WRITE    1
#define DIRECTION_READ     0
#define CLKDIV_DIVIDE_BY_1 0

#endif /* ZEPHYR_DRIVERS_I2C_TI_UNICOMM_H_ */
