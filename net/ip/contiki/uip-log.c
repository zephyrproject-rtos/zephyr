#include <misc/printk.h>

#if defined(CONFIG_STDOUT_CONSOLE)
#include <stdio.h>
#define PRINT           printf
#else
#include <misc/printk.h>
#define PRINT           printk
#endif

#include "ip/uipopt.h"
#include "sys/log.h"

#if UIP_LOGGING
void uip_log(char *message)
{
	PRINT(message);
	PRINT("\n");
}
#endif /* UIP_LOGGING */

#if LOG_CONF_ENABLED
void log_message(const char *part1, const char *part2)
{
	PRINT(part1);
	PRINT(part2);
	PRINT("\n");
}
#endif /* LOG_CONF_ENABLED */
