	LOG_INF("RGB histogram of the image");
	pixel_rgb24frame_to_rgb24hist(image_rgb24, sizeof(image_rgb24),
				 rgb24hist, ARRAY_SIZE(rgb24hist), NVAL);
	pixel_print_rgb24hist(rgb24hist, ARRAY_SIZE(rgb24hist), 16);

