static uint8_t yuyvframe[WIDTH * HEIGHT * 2] = {0};

/* Stream conversion steps are declared at build time */
PIXEL_RGB24STREAM_TO_RGB565LESTREAM(step_rgb24_to_rgb565, WIDTH, HEIGHT);
PIXEL_RGB565LESTREAM_TO_RGB24STREAM(step_rgb565_to_rgb24, WIDTH, HEIGHT);
PIXEL_RGB24STREAM_TO_RGB332STREAM(step_rgb24_to_rgb332, WIDTH, HEIGHT);
PIXEL_RGB332STREAM_TO_RGB24STREAM(step_rgb332_to_rgb24, WIDTH, HEIGHT);
PIXEL_RGB24STREAM_TO_YUYVSTREAM_BT709(step_rgb24_to_yuyv, WIDTH, HEIGHT);

int main(void)
{
	LOG_INF("Input image preview:");
	pixel_print_rgb24frame_truecolor(rgb24frame, sizeof(rgb24frame), WIDTH, HEIGHT);

	/* Launch a new stream pipeline made out of the following blocks */
	pixel_stream_to_yuyvframe(rgb24frame, sizeof(rgb24frame), WIDTH,
				  yuyvframe, sizeof(yuyvframe), WIDTH,
				  &step_rgb24_to_rgb565, &step_rgb565_to_rgb24,
				  &step_rgb24_to_rgb332, &step_rgb332_to_rgb24,
				  &step_rgb24_to_yuyv, NULL);

	LOG_INF("Output image preview:");
	pixel_print_yuyvframe_bt709_truecolor(yuyvframe, sizeof(yuyvframe), WIDTH, HEIGHT);

	return 0;
}
