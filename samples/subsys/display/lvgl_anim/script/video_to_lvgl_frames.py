#!/usr/bin/env python3

import argparse
import json
import os
import shutil
import subprocess
import sys
import tempfile

try:
    from PIL import Image
except ImportError:
    sys.exit("[ERROR] Pillow not found.  Run:  pip install Pillow")

FIT_MODES = ["crop", "letterbox", "stretch"]

COLS_PER_LINE = {
    "rgb565": 16,
    "rgb888": 8,
    "argb8888": 8,
}

FORMATS = {
    "rgb565": {
        "lv_cf": "LV_COLOR_FORMAT_RGB565",
        "bpp": 2,
        "dtype": "uint16_t",
        "per_px": 1,
    },
    "rgb888": {
        "lv_cf": "LV_COLOR_FORMAT_RGB888",
        "bpp": 3,
        "dtype": "uint8_t",
        "per_px": 3,
    },
    "argb8888": {
        "lv_cf": "LV_COLOR_FORMAT_ARGB8888",
        "bpp": 4,
        "dtype": "uint32_t",
        "per_px": 1,
    },
}


def _bold(s):
    return f"\033[1m{s}\033[0m"


def _red(s):
    return f"\033[91m{s}\033[0m"


def to_rgb565(r, g, b, a=255):
    val = ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3)
    return f"0x{val:04X}"


def to_rgb888(r, g, b, a=255):
    return f"0x{r:02X}, 0x{g:02X}, 0x{b:02X}"


def to_argb8888(r, g, b, a=255):
    val = (a << 24) | (r << 16) | (g << 8) | b
    return f"0x{val:08X}"


PIXEL_FN = {
    "rgb565": to_rgb565,
    "rgb888": to_rgb888,
    "argb8888": to_argb8888,
}


def probe_video(path):
    """Return (width, height, fps_float, duration_s, total_frames)."""
    cmd = [
        "ffprobe",
        "-v",
        "quiet",
        "-print_format",
        "json",
        "-show_streams",
        path,
    ]
    try:
        out = subprocess.check_output(cmd, stderr=subprocess.DEVNULL)
    except subprocess.CalledProcessError:
        sys.exit(_red(f"[ERROR] ffprobe failed on '{path}'"))

    data = json.loads(out)
    vstream = next(
        (s for s in data.get("streams", []) if s.get("codec_type") == "video"),
        None,
    )
    if not vstream:
        sys.exit(_red("[ERROR] No video stream found."))

    w = int(vstream["width"])
    h = int(vstream["height"])

    def parse_rate(s):
        num, den = s.split("/")
        return float(num) / float(den) if float(den) else 0.0

    fps = parse_rate(vstream.get("avg_frame_rate") or vstream.get("r_frame_rate", "0/1"))
    dur = float(vstream.get("duration", 0))
    total = int(vstream.get("nb_frames", 0)) or int(fps * dur)

    return w, h, fps, dur, total


def extract_frames(video, out_dir, num_frames, total_frames, out_w, out_h, src_w, src_h, fit):
    """
    Extract `num_frames` evenly-spaced frames from `video`, written as PNGs
    into `out_dir`.  Applies scaling + fit filter.
    """
    step = max(1, total_frames // num_frames)

    if fit == "stretch":
        scale_filter = f"scale={out_w}:{out_h}"
    elif fit == "letterbox":
        # Scale to fit inside out_w x out_h, pad remainder with black
        scale_filter = (
            f"scale={out_w}:{out_h}:force_original_aspect_ratio=decrease,"
            f"pad={out_w}:{out_h}:(ow-iw)/2:(oh-ih)/2:black"
        )
    else:
        scale_w = out_w
        scale_h = int(out_w * src_h / src_w)
        if scale_h < out_h:
            scale_h = out_h
            scale_w = int(out_h * src_w / src_h)
        scale_filter = f"scale={scale_w}:{scale_h},crop={out_w}:{out_h}"

    vf = f"select='not(mod(n\\,{step}))',{scale_filter}"

    cmd = [
        "ffmpeg",
        "-y",
        "-i",
        video,
        "-vf",
        vf,
        "-vframes",
        str(num_frames),
        "-vsync",
        "vfr",
        os.path.join(out_dir, "frame_%03d.png"),
    ]

    print(_bold(f"\n  Running ffmpeg (fit={fit}, {out_w}x{out_h})..."))
    result = subprocess.run(cmd, capture_output=True)
    if result.returncode != 0:
        print(result.stderr.decode())
        sys.exit(_red("[ERROR] ffmpeg failed."))

    pngs = sorted(f for f in os.listdir(out_dir) if f.endswith(".png"))
    return pngs


def write_frame_header(path, var_name, img, fmt_key, w, h):
    fmt = FORMATS[fmt_key]
    px_fn = PIXEL_FN[fmt_key]
    cols = COLS_PER_LINE[fmt_key]
    stride = w * fmt["bpp"]
    guard = f"{var_name.upper()}_H"
    mode = "RGBA" if fmt_key == "argb8888" else "RGB"
    img = img.convert(mode)
    raw = img.tobytes()
    n_ch = 4 if mode == "RGBA" else 3
    n_px = w * h

    with open(path, "w") as f:
        f.write("/* Auto-generated by video_to_lvgl_frames.py -- DO NOT EDIT */\n")
        f.write(f"#ifndef {guard}\n#define {guard}\n\n")
        f.write('#include "lvgl.h"\n\n')
        f.write(f"#define {var_name.upper()}_WIDTH  {w}U\n")
        f.write(f"#define {var_name.upper()}_HEIGHT {h}U\n\n")
        f.write(f"static const {fmt['dtype']} {var_name}_map[{w * h * fmt['per_px']}] = {{\n")

        for i in range(n_px):
            if i % cols == 0:
                f.write("    ")
            base = i * n_ch
            r = raw[base]
            g = raw[base + 1]
            b = raw[base + 2]
            a = raw[base + 3] if n_ch == 4 else 255
            f.write(px_fn(r, g, b, a) + ",")
            f.write("\n" if i % cols == cols - 1 else " ")
        f.write("\n};\n\n")

        f.write(f"static const lv_image_dsc_t {var_name} = {{\n")
        f.write("    .header = {\n")
        f.write("        .magic  = LV_IMAGE_HEADER_MAGIC,\n")
        f.write(f"        .cf     = {fmt['lv_cf']},\n")
        f.write("        .flags  = 0,\n")
        f.write(f"        .w      = {w}U,\n")
        f.write(f"        .h      = {h}U,\n")
        f.write(f"        .stride = {stride}U,\n")
        f.write("    },\n")
        f.write(f"    .data_size = sizeof({var_name}_map),\n")
        f.write(f"    .data      = (const uint8_t *){var_name}_map,\n")
        f.write("};\n\n")
        f.write(f"#endif /* {guard} */\n")


def write_master_header(path, frame_names, fmt_key, w, h, source, dur_s):
    n = len(frame_names)
    duration_ms = max(1, round(dur_s * 1000))

    with open(path, "w") as f:
        f.write("/* Auto-generated by video_to_lvgl_frames.py — DO NOT EDIT */\n")
        f.write("#ifndef LVGL_ANIMATION_FRAMES_H\n#define LVGL_ANIMATION_FRAMES_H\n\n")
        f.write('#include "lvgl.h"\n\n')

        for name in frame_names:
            f.write(f'#include "{name}.h"\n')

        f.write(f"\n#define LVGL_ANIMATION_FRAME_COUNT   {n}U\n")
        f.write(f"#define LVGL_ANIMATION_DURATION_MS   {duration_ms}U  /* {dur_s:.3f} s */\n\n")

        f.write(
            "static const lv_image_dsc_t * const lvgl_anim_frames[LVGL_ANIMATION_FRAME_COUNT] = {\n"
        )
        for name in frame_names:
            f.write(f"    &{name},\n")
        f.write("};\n\n")

        f.write("/* Animation exec callback for lv_anim_set_exec_cb() */\n")
        f.write("static inline void lvgl_anim_set_frame(void *var, int32_t idx)\n{\n")
        f.write("    lv_image_set_src((lv_obj_t *)var,\n")
        f.write("                     lvgl_anim_frames[(uint32_t)idx]);\n")
        f.write("}\n\n")
        f.write("#endif /* LVGL_ANIMATION_FRAMES_H */\n")


def main():
    parser = argparse.ArgumentParser(
        description="Convert a video into LVGL C image header files.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        allow_abbrev=False,
    )
    parser.add_argument("video", help="Input video file")
    parser.add_argument("--width", type=int)
    parser.add_argument("--height", type=int)
    parser.add_argument("--frames", type=int)
    parser.add_argument("--format", choices=list(FORMATS.keys()))
    parser.add_argument("--fit", choices=FIT_MODES)
    parser.add_argument("--out", default="../img")
    parser.add_argument("--prefix", default="frame")
    args = parser.parse_args()

    video = args.video
    if not os.path.isfile(video):
        sys.exit(_red(f"[ERROR] File not found: '{video}'"))

    print(_bold(f"\n  Probing '{os.path.basename(video)}'..."))
    src_w, src_h, fps, dur, total = probe_video(video)
    print(f"  Source  : {src_w}x{src_h}  |  {fps:.2f} fps  |  {dur:.2f}s  |  ~{total} frames")

    out_w = args.width
    out_h = args.height
    num_frames = args.frames
    fmt_key = args.format
    fit = args.fit
    prefix = args.prefix

    img_dir = os.path.normpath(os.path.join(os.path.abspath(args.out), "..", "img"))
    if os.path.isdir(img_dir):
        shutil.rmtree(img_dir)

    if os.path.isdir(args.out):
        shutil.rmtree(args.out)

    os.makedirs(args.out, exist_ok=True)

    with tempfile.TemporaryDirectory() as tmp:
        pngs = extract_frames(
            video,
            tmp,
            num_frames,
            total,
            out_w,
            out_h,
            src_w,
            src_h,
            fit,
        )

        if not pngs:
            sys.exit(_red("[ERROR] No frames were extracted."))

        actual = len(pngs)
        if actual < num_frames:
            print(_red(f"  Warning: requested {num_frames}, got {actual} frames."))

        frame_names = []

        for i, png in enumerate(pngs, start=1):
            img = Image.open(os.path.join(tmp, png))
            var_name = f"{prefix}_{i:03d}"
            frame_names.append(var_name)
            out_path = os.path.join(args.out, f"{var_name}.h")
            write_frame_header(out_path, var_name, img, fmt_key, out_w, out_h)
            bar = "#" * i + "." * (actual - i)
            print(f"\r  [{bar}] {i}/{actual}  {var_name}.h", end="", flush=True)

        master = os.path.join(args.out, "lvgl_anim_frames.h")
        write_master_header(master, frame_names, fmt_key, out_w, out_h, video, dur)

    print('\n')
    print(_bold("  prj.conf:"))
    depth = {"rgb565": 16, "rgb888": 32, "argb8888": 32}[fmt_key]
    print(f"    CONFIG_LV_COLOR_DEPTH_{depth}=y")
    print()


if __name__ == "__main__":
    main()
