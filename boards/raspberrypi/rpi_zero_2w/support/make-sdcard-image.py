"""
make-sdcard-image.py -- build a self-contained microSD image that boots
Zephyr on the Raspberry Pi Zero 2 W.

The output is a flat .img file with one FAT32 partition containing:
    bootcode.bin       (Pi VPU bootloader)
    fixup.dat          (VPU memory split config)
    start.elf          (VPU firmware that loads the ARM kernel)
    config.txt         (Zephyr-required boot params)
    zephyr.bin         (the built image, linked at 0x200000)

The Pi firmware blobs are downloaded once from the upstream
raspberrypi/firmware repo on github (pinned to FW_TAG below) and cached
under ~/.cache/zephyr-rpi-zero-2w/<tag>/. Subsequent runs reuse them.

The output .img can be flashed with `Use custom` in Raspberry Pi
Imager, or written to a card directly via `dd` / `balenaEtcher`.

Usage:
    make-sdcard-image.py path/to/zephyr.bin [-o sdcard.img] [--size-mb 64]

Requirements:
    Python >= 3.8
    mtools (mformat, mcopy)
        macOS:          brew install mtools
        Debian/Ubuntu:  sudo apt install mtools
"""

import argparse
import shutil
import struct
import subprocess
import sys
import urllib.request
from pathlib import Path

# Pin to a specific firmware tag for reproducibility. Bump as needed.
FW_TAG = "1.20240819"
FW_BASE_URL = f"https://github.com/raspberrypi/firmware/raw/{FW_TAG}/boot/"
FW_FILES = ("bootcode.bin", "fixup.dat", "start.elf")

BOOT_CONFIG_TXT = b"""\
# Generated by zephyr/boards/raspberrypi/rpi_zero_2w/support/make-sdcard-image.py.

arm_64bit=1
enable_uart=1
core_freq=250
kernel_address=0x200000
kernel=zephyr.bin
"""


def cache_dir() -> Path:
    p = Path.home() / ".cache" / "zephyr-rpi-zero-2w" / FW_TAG
    p.mkdir(parents=True, exist_ok=True)
    return p


def fetch_firmware(cache: Path) -> None:
    for name in FW_FILES:
        dest = cache / name
        if dest.exists() and dest.stat().st_size > 0:
            continue
        url = FW_BASE_URL + name
        print(f"  fetching {name}...", file=sys.stderr)
        try:
            urllib.request.urlretrieve(url, dest)
        except Exception as e:
            sys.exit(f"error: failed to download {url}: {e}")


def write_mbr(image_path: Path, fs_start_sector: int, fs_sectors: int) -> None:
    """Write a single-FAT32-LBA-partition MBR at the start of the image."""
    pe = struct.pack(
        "<B3sB3sLL",
        0x80,  # bootable flag
        b"\x00\x02\x00",  # CHS start (don't care for LBA)
        0x0C,  # FAT32 LBA partition type
        b"\xfe\xff\xff",  # CHS end (sentinel)
        fs_start_sector,
        fs_sectors,
    )
    with image_path.open("r+b") as f:
        f.seek(0x1BE)
        f.write(pe)
        f.seek(0x1FE)
        f.write(b"\x55\xaa")


def need(tool: str) -> str:
    path = shutil.which(tool)
    if path is None:
        sys.exit(
            f"error: '{tool}' not found in PATH. "
            "Install mtools: `brew install mtools` or `apt install mtools`."
        )
    return path


def main() -> None:
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
        allow_abbrev=False,
    )
    parser.add_argument("zephyr_bin", type=Path, help="Path to zephyr.bin")
    parser.add_argument(
        "-o",
        "--output",
        type=Path,
        default=Path("rpi_zero_2w-zephyr.img"),
        help="Output image path (default: ./rpi_zero_2w-zephyr.img)",
    )
    parser.add_argument(
        "--size-mb",
        type=int,
        default=64,
        help="Image size in MiB (default: 64)",
    )
    args = parser.parse_args()

    if not args.zephyr_bin.is_file():
        sys.exit(f"error: {args.zephyr_bin}: not found")

    mformat = need("mformat")
    mcopy = need("mcopy")

    cache = cache_dir()
    fetch_firmware(cache)

    # Drop the generated config.txt next to the cached firmware blobs so
    # mcopy can pick it up uniformly.
    (cache / "config.txt").write_bytes(BOOT_CONFIG_TXT)

    sector_size = 512
    fs_start_sector = 2048  # 1 MiB partition offset (typical)
    total_sectors = args.size_mb * 1024 * 1024 // sector_size
    fs_sectors = total_sectors - fs_start_sector

    print(f"creating {args.output} ({args.size_mb} MiB)...", file=sys.stderr)
    with args.output.open("wb") as f:
        f.truncate(args.size_mb * 1024 * 1024)

    write_mbr(args.output, fs_start_sector, fs_sectors)

    fat_offset = fs_start_sector * sector_size
    img_at = f"{args.output}@@{fat_offset}"

    print("formatting FAT32 partition...", file=sys.stderr)
    subprocess.run(
        [mformat, "-i", img_at, "-F", "-T", str(fs_sectors), "-N", "00000000"],
        check=True,
    )

    print("copying boot files...", file=sys.stderr)
    for name in FW_FILES + ("config.txt",):
        subprocess.run(
            [mcopy, "-i", img_at, str(cache / name), "::"],
            check=True,
        )
    subprocess.run(
        [mcopy, "-i", img_at, str(args.zephyr_bin), "::zephyr.bin"],
        check=True,
    )

    print(f"\ndone: {args.output}", file=sys.stderr)
    print("Flash via Raspberry Pi Imager (`Use custom`) or:", file=sys.stderr)
    print(f"  dd if={args.output} of=/dev/diskN bs=1m  # macOS", file=sys.stderr)
    print(f"  dd if={args.output} of=/dev/sdX  bs=1M  # Linux", file=sys.stderr)


if __name__ == "__main__":
    main()
