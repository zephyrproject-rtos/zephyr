# install-to-sdcard.sh -- drop a Zephyr build onto an already-imaged
# Raspberry Pi boot partition so the Pi Zero W boots Zephyr instead.
#
# Usage:
#   install-to-sdcard.sh <bootfs-mount-point> <path-to-zephyr.bin>
#
# Example (macOS, PINN/RPi-OS imaged card):
#   install-to-sdcard.sh /Volumes/RECOVERY build/rpizw-hw/zephyr/zephyr.bin
#
# The script:
#   1. Copies zephyr.bin into the boot partition.
#   2. Replaces config.txt with the ARMv6 Zephyr boot params,
#      preserving the original as config.txt.orig (only on first run).
#
# Pi firmware blobs (bootcode.bin, fixup.dat, start.elf) are untouched.

set -e

BOOTFS="$1"
ZEPHYR_BIN="$2"

usage() {
	cat >&2 <<EOF
Usage: $0 <bootfs-mount-point> <path-to-zephyr.bin>

Example (macOS):
  $0 /Volumes/RECOVERY build/rpizw-hw/zephyr/zephyr.bin
EOF
	exit 1
}

if [ -z "$BOOTFS" ] || [ -z "$ZEPHYR_BIN" ]; then
	usage
fi

if [ ! -d "$BOOTFS" ]; then
	echo "error: $BOOTFS is not a directory (is the SD card mounted?)" >&2
	exit 1
fi
if [ ! -f "$ZEPHYR_BIN" ]; then
	echo "error: $ZEPHYR_BIN: file not found" >&2
	exit 1
fi
if [ ! -f "$BOOTFS/bootcode.bin" ] || [ ! -f "$BOOTFS/start.elf" ]; then
	echo "error: $BOOTFS does not look like a Pi boot partition" >&2
	echo "       (no bootcode.bin or start.elf present)" >&2
	exit 1
fi

cp "$ZEPHYR_BIN" "$BOOTFS/zephyr.bin"

if [ -f "$BOOTFS/config.txt" ] && [ ! -f "$BOOTFS/config.txt.orig" ]; then
	cp "$BOOTFS/config.txt" "$BOOTFS/config.txt.orig"
fi

cat > "$BOOTFS/config.txt" <<'EOF'
# Generated by zephyr/boards/raspberrypi/rpi_zero_w/support/install-to-sdcard.sh.
# The original config.txt is preserved as config.txt.orig.

# BCM2835 / ARM1176 is 32-bit; GPU loads the raw kernel at 0x8000.
kernel=zephyr.bin
kernel_address=0x8000

# This port does not consume ATAGS; tell the firmware not to write them.
disable_commandline_tags=1

# Console is the AUX mini-UART (UART1) on GPIO 14/15. enable_uart=1 makes
# the firmware enable the AUX block, mux GPIO 14/15 to the mini-UART, and
# pin the core clock so the mini-UART baud divisor stays valid (its clock
# is the core clock, unlike PL011). PL011 is left on the Bluetooth modem.
enable_uart=1
core_freq=250
EOF

echo "Installed $(basename "$ZEPHYR_BIN") to $BOOTFS"
echo "Eject the card and boot the Pi (console: GPIO 14/15, 115200 8N1)."
