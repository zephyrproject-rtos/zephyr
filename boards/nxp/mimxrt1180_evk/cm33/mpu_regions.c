#include <zephyr/devicetree.h>
#include <zephyr/arch/arm/cortex_m/arm_mpu_mem_cfg.h>

#define REGION_FLEXSPI2_BASE_ADDRESS   DT_REG_ADDR_BY_IDX(DT_NODELABEL(flexspi2), 1)
#define REGION_FLEXSPI2_SIZE           DT_REG_SIZE_BY_IDX(DT_NODELABEL(flexspi2), 1)
#define REGION_ITCM_BASE_ADDRESS       DT_REG_ADDR_BY_IDX(DT_NODELABEL(itcm), 0)
#define REGION_ITCM_SIZE               DT_REG_SIZE_BY_IDX(DT_NODELABEL(itcm), 0)
#define REGION_DTCM_BASE_ADDRESS       DT_REG_ADDR_BY_IDX(DT_NODELABEL(dtcm), 0)
#define REGION_DTCM_SIZE               DT_REG_SIZE_BY_IDX(DT_NODELABEL(dtcm), 0)
#define REGION_SRAM1_SHM_BASE_ADDRESS  DT_REG_ADDR_BY_IDX(DT_NODELABEL(ocram1), 0)
#define REGION_SRAM1_SHM_SIZE          DT_REG_SIZE_BY_IDX(DT_NODELABEL(ocram1), 0)
#define REGION_SRAM2_SHM_BASE_ADDRESS  DT_REG_ADDR_BY_IDX(DT_NODELABEL(ocram2), 0)
#define REGION_SRAM2_SHM_SIZE          DT_REG_SIZE_BY_IDX(DT_NODELABEL(ocram2), 0)
#define REGION_FLEXSPI_BASE_ADDRESS    DT_REG_ADDR_BY_IDX(DT_NODELABEL(flexspi), 1)
#define REGION_FLEXSPI_SIZE            DT_REG_SIZE_BY_IDX(DT_NODELABEL(flexspi), 1)
#define REGION_PERIPHERAL_BASE_ADDRESS DT_REG_ADDR_BY_IDX(DT_NODELABEL(peripheral), 0)
#define REGION_PERIPHERAL_SIZE         DT_REG_SIZE_BY_IDX(DT_NODELABEL(peripheral), 0)

static const struct arm_mpu_region mpu_regions[] = {
	MPU_REGION_ENTRY("FLEXSPI2", REGION_FLEXSPI2_BASE_ADDRESS,
			 REGION_RAM_ATTR(REGION_FLEXSPI2_BASE_ADDRESS, REGION_FLEXSPI2_SIZE)),
	MPU_REGION_ENTRY("FLEXSPI", REGION_FLEXSPI_BASE_ADDRESS,
			 REGION_FLASH_ATTR(REGION_FLEXSPI_BASE_ADDRESS, REGION_FLEXSPI_SIZE)),
	MPU_REGION_ENTRY("ITCM", REGION_ITCM_BASE_ADDRESS,
			 REGION_RAM_NOCACHE_ATTR(REGION_ITCM_BASE_ADDRESS, REGION_ITCM_SIZE)),
	MPU_REGION_ENTRY("DTCM", REGION_DTCM_BASE_ADDRESS,
			 REGION_RAM_NOCACHE_ATTR(REGION_DTCM_BASE_ADDRESS, REGION_DTCM_SIZE)),
	MPU_REGION_ENTRY("SRAM1_SHM", REGION_SRAM1_SHM_BASE_ADDRESS,
			 REGION_RAM_ATTR(REGION_SRAM1_SHM_BASE_ADDRESS, REGION_SRAM1_SHM_SIZE)),
	MPU_REGION_ENTRY(
		"SRAM2_SHM", REGION_SRAM2_SHM_BASE_ADDRESS,
		REGION_RAM_NOCACHE_ATTR(REGION_SRAM2_SHM_BASE_ADDRESS, REGION_SRAM2_SHM_SIZE)),
	MPU_REGION_ENTRY(
		"PERIPHERAL", REGION_PERIPHERAL_BASE_ADDRESS,
		REGION_DEVICE_ATTR(REGION_PERIPHERAL_BASE_ADDRESS, REGION_PERIPHERAL_SIZE)),
};

const struct arm_mpu_config mpu_config = {
	.num_regions = ARRAY_SIZE(mpu_regions),
	.mpu_regions = mpu_regions,
};
