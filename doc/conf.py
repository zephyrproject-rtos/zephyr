def _set_html_permalinks_icon(_, config):
    config.html_permalinks_icon = ""


# Domains whose targets are not generated when the corresponding layer is
# skipped (see SKIP_* flags above). Cross-references into these are degraded to
# plain text so partial builds don't fail under ``-W``.
_DEGRADE_REF_DOMAINS = set()
# Within the broad "zephyr" domain only specific reftypes are degraded, so that
# genuine mistakes in other zephyr roles still surface.
_DEGRADE_ZEPHYR_REFTYPES = set()
if SKIP_DOXYGEN:
    _DEGRADE_REF_DOMAINS |= {"c", "cpp"}
if SKIP_KCONFIG:
    _DEGRADE_REF_DOMAINS.add("kconfig")
if SKIP_BOARD_CATALOG:
    _DEGRADE_ZEPHYR_REFTYPES |= {"board", "board-catalog"}
if SKIP_EXTERNAL_CONTENT:
    _DEGRADE_ZEPHYR_REFTYPES |= {"board", "board-catalog", "code-sample", "code-sample-category"}
if _DEGRADE_ZEPHYR_REFTYPES:
    _DEGRADE_REF_DOMAINS.add("zephyr")


def _degrade_missing_reference(app, env, node, contnode):
    """Silence cross-references into autogenerated layers skipped by this build.

    Returning a node marks the reference as resolved (rendered as plain inline
    text), so a partial build does not fail under ``-W``. References into layers
    that *are* built still warn normally.
    """
    domain = node.get("refdomain")
    if domain not in _DEGRADE_REF_DOMAINS:
        return None
    if domain == "zephyr" and node.get("reftype") not in _DEGRADE_ZEPHYR_REFTYPES:
        return None
    return contnode


def setup(app):
    # theme customizations
    app.add_css_file("css/custom.css")
    app.add_js_file("js/custom.js")
    # RTD theme hard codes a Font Awesome link icon in its setup() code, but we want no icon
    app.connect("config-inited", _set_html_permalinks_icon, priority=900)

    # Partial build: degrade dangling references into skipped layers.
    if _DEGRADE_REF_DOMAINS:
        app.connect("missing-reference", _degrade_missing_reference)
