"""
Doxygen Coverage Diff Tool

Compares Doxygen documentation coverage between two coverxygen JSON reports
(reference and comparison branches) and identifies newly introduced undocumented
API symbols.

Outputs GitHub Actions annotations for undocumented new symbols and fails if any
are found.

Usage:
    python3 doxygen_coverage_diff.py --reference base.json --comparison pr.json

The JSON files should be generated by coverxygen with --format json-v3.
"""

import argparse
import json
from collections import Counter, defaultdict
from pathlib import Path
from typing import NamedTuple


class Symbol(NamedTuple):
    """A documentable API symbol."""

    file: str
    line: int
    name: str
    kind: str
    documented: bool


def extract_symbols(data: dict, strip_prefix: str = None) -> list[Symbol]:
    """Extract all symbols from coverxygen json-v3 format as a flat list."""
    symbols = []
    for path, sym_list in data.get("files", {}).items():
        if strip_prefix and path.startswith(strip_prefix):
            path = path.removeprefix(strip_prefix).lstrip("/")
        if not isinstance(sym_list, list):
            continue
        symbols.extend(
            Symbol(
                path,
                s.get("line", 0),
                s.get("symbol", ""),
                s.get("kind", ""),
                s.get("documented", False),
            )
            for s in sym_list
        )
    return symbols


def find_undocumented_new(ref: list[Symbol], comp: list[Symbol]) -> list[Symbol]:
    """
    Find symbols that are newly introduced and undocumented.

    Calculates net-new undocumented count per file. If a file has N undocumented
    in PR and M in base, only reports new symbols if N > M.
    """

    def group_undoc_by_file(syms):
        """Group undocumented symbols by their source file."""
        by_file = defaultdict(list)
        for s in syms:
            if not s.documented:
                by_file[s.file].append(s)
        return by_file

    ref_undoc, comp_undoc = group_undoc_by_file(ref), group_undoc_by_file(comp)
    result = []

    for fname, comp_list in comp_undoc.items():
        ref_list = ref_undoc.get(fname, [])
        net = len(comp_list) - len(ref_list)
        if net <= 0:
            continue

        # Find symbols in comparison but not in reference using Counter for duplicates
        ref_counts = Counter((s.name, s.kind) for s in ref_list)
        candidates = []
        for s in comp_list:
            key = (s.name, s.kind)
            if ref_counts[key] > 0:
                ref_counts[key] -= 1
            else:
                candidates.append(s)

        candidates.sort(key=lambda s: s.line)
        result.extend(candidates[:net])

    return sorted(result, key=lambda s: (s.file, s.line))


def output_github_annotations(symbols: list[Symbol], max_annotations: int = 50) -> None:
    """Output GitHub Actions workflow annotations for undocumented symbols."""
    for sym in symbols[:max_annotations]:
        print(
            f"::error file={sym.file},line={sym.line},"
            f"title=Missing Doxygen documentation::{sym.kind} '{sym.name}'"
            " is missing Doxygen comments."
        )

    if len(symbols) > max_annotations:
        print(
            f"::warning::... and {len(symbols) - max_annotations} more undocumented symbols "
            f"(showing first {max_annotations})"
        )


def write_summary(symbols: list[Symbol], summary_file: str) -> None:
    """Write a markdown summary table to the given file."""
    with open(summary_file, "a", encoding="utf-8") as f:
        f.write("\n## 📚 Doxygen Coverage Check Results\n\n")

        if not symbols:
            f.write("✅ **All new API symbols are properly documented!**\n")
            return

        f.write(f"> [!CAUTION]\n> **{len(symbols)} new API symbol(s) are missing documentation.**")
        f.write("\n\n")
        f.write("| File | Line | Kind | Symbol |\n|------|------|------|--------|\n")

        for sym in symbols[:50]:
            f.write(f"| `{sym.file}` | {sym.line} | {sym.kind} | `{sym.name}` |\n")

        if len(symbols) > 50:
            f.write(f"\n*... and {len(symbols) - 50} more*\n")

        f.write(
            "\n### How to fix\n\n"
            "Add Doxygen documentation comments to the listed symbols (or properly hide them "
            "from the public API documentation if they are internal).\n\n"
            "See the project's [Doxygen style guidelines]"
            "(https://docs.zephyrproject.org/latest/contribute/style/doxygen.html).\n"
        )


def main():
    parser = argparse.ArgumentParser(
        description="Compare Doxygen coverage between base and PR branches",
        allow_abbrev=False,
    )
    parser.add_argument(
        "--reference", required=True, help="Path to reference coverage JSON (coverxygen json-v3)"
    )
    parser.add_argument(
        "--comparison", required=True, help="Path to comparison coverage JSON (coverxygen json-v3)"
    )
    parser.add_argument(
        "--summary-file", help="Path to write markdown summary (e.g., $GITHUB_STEP_SUMMARY)"
    )
    parser.add_argument(
        "--strip-reference-prefix", help="Path prefix to strip from reference file paths"
    )
    parser.add_argument(
        "--strip-comparison-prefix", help="Path prefix to strip from comparison file paths"
    )
    args = parser.parse_args()

    # Validate inputs
    for name, path in [("Reference", args.reference), ("Comparison", args.comparison)]:
        if not Path(path).is_file():
            print(f"::error::{name} coverage file not found: {path}")
            return 1

    # Load and process
    try:
        ref_data = json.loads(Path(args.reference).read_text(encoding="utf-8"))
        comp_data = json.loads(Path(args.comparison).read_text(encoding="utf-8"))
    except json.JSONDecodeError as e:
        print(f"::error::Failed to parse coverage JSON: {e}")
        return 1

    ref_symbols = extract_symbols(ref_data, args.strip_reference_prefix)
    comp_symbols = extract_symbols(comp_data, args.strip_comparison_prefix)

    print(f"Reference: {len(ref_symbols)} symbols")
    print(f"Comparison: {len(comp_symbols)} symbols")

    undocumented_new = find_undocumented_new(ref_symbols, comp_symbols)
    print(f"New undocumented symbols: {len(undocumented_new)}")

    if undocumented_new:
        output_github_annotations(undocumented_new)

    if args.summary_file:
        write_summary(undocumented_new, args.summary_file)

    if undocumented_new:
        print(f"\n❌ Found {len(undocumented_new)} new API symbol(s) without documentation.")
        return 1

    print("\n✅ All new API symbols are properly documented!")
    return 0


