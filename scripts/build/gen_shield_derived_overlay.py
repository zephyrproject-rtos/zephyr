import re
import os
import argparse


def process_shield_file(shield_overlay, shield_optstr, output_dir):
    """
    Processes a single shield file, generating an overlay based on its name and content.

    Args:
        shield_overlay (str): The full path to the shield file to process.
        output_dir (str): The directory to store generated overlays.

    Returns:
        None
    """
    match = re.match(
        r"^([a-zA-Z_][a-zA-Z0-9_]*)(@[a-zA-Z_][a-zA-Z0-9_]+)?(.*)?$",
        shield_optstr
    )
    if not match:
        raise ValueError(f"Invalid shield file name format: {shield_overlay}")

    # Extract parts of the shield file name
    opts_list = []
    s = match.group(1)  # stem part
    base_name = s.upper()

    if match.group(3):
        sopts = match.group(3)[1:-1]  # options part
        opts_list = sopts.split(":")

    shield_optstr_ident = re.sub(r"[^a-zA-Z0-9_]", "_", shield_optstr)
    derived_name = shield_optstr_ident.upper()

    # Derive shield_dir from shield_overlay
    shield_dir = os.path.dirname(shield_overlay)

    # Define the output overlay file path
    shield_name = os.path.splitext(os.path.basename(shield_overlay))[0]
    derived_overlay = os.path.join(
        output_dir, shield_name + ":" + shield_optstr + ".overlay"
    )
    definitions = []

    # Generate option macros
    for opt in opts_list:
        if not opt:
            continue
        opt_match = re.match(r"^([a-zA-Z_][a-zA-Z0-9_]+)(=[a-zA-Z0-9_]*)?$", opt)
        if not opt_match:
            continue

        opt_name = opt_match.group(1)
        opt_value = opt_match.group(2)[1:] if opt_match.group(2) else "1"

        opt_fullname_upper = f"SHIELD_OPTION_{derived_name}_{opt_name}".upper()
        definitions.append(f"#define {opt_fullname_upper}_DEFINED 1\n")
        definitions.append(f"#define {opt_fullname_upper} {opt_value}\n")

    # Create overlay content
    overlay_content = (
        "/* auto-generated by shields.py, don't edit */\n"
        "\n"
        "#include <shield_utils.h>\n"
        "\n" + "".join(definitions) + f"\n#define SHIELD_BASE_NAME {base_name}\n"
        "#define SHIELD_BASE_NAME_DEFINED 1\n"
        f"#define SHIELD_DERIVED_NAME {derived_name}\n"
        "#define SHIELD_DERIVED_NAME_DEFINED 1\n"
        "\n"
        f"#include <{shield_overlay}>\n"
        "\n"
        "#undef SHIELD_DERIVED_NAME_DEFINED\n"
        "#undef SHIELD_DERIVED_NAME\n"
        "#undef SHIELD_BASE_NAME_DEFINED\n"
        "#undef SHIELD_BASE_NAME\n"
    )

    os.makedirs(os.path.dirname(derived_overlay), exist_ok=True)
    # Write the content to a new file and move it to the target location
    with open(f"{derived_overlay}.new", "w") as file:
        file.write(overlay_content)

    os.replace(f"{derived_overlay}.new", derived_overlay)


def main():
    parser = argparse.ArgumentParser(
        description="Process shield files and generate overlays."
    )
    parser.add_argument("shield_overlay", type=str, help="The full path to the shield overlay file.")
    parser.add_argument(
        "--output-dir", required=True, type=str,
        help="The directory to store generated overlays."
    )
    parser.add_argument(
        "--shield-options", required=True, type=str,
        help="Optional shield options as a colon-separated string (e.g., option1:option2)."
    )

    args = parser.parse_args()

    try:
        process_shield_file(args.shield_overlay, args.shield_options, args.output_dir)
        print(f"Overlay generated successfully for {args.shield_overlay}.")
    except Exception as e:
        print(f"Error: {e}")


if __name__ == "__main__":
    main()
