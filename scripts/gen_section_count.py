# gen_section_count.py, extract an iterable-section item count from a
# fully-linked Zephyr ELF and emit a C header.
#
# In the final ELF the linker merges all  ._<section>.static.*  subsections
# into one contiguous  <section>  section. We read that single merged section.
#
# Usage:
#     gen_section_count.py --section <name> --type <c-type>
#                          --output <header>
#                          <zephyr.elf>
#
# Dependencies:
#     pip install pyelftools

import sys
import argparse
import pathlib
from elftools.elf.elffile import ELFFile
from elftools.common.exceptions import ELFError


def get_merged_section_size(elf_path: pathlib.Path,
                             section_name: str) -> int:
    """
    Return the size of the merged section in the final linked ELF.
    Falls back to summing  ._<section_name>.*  subsections if the
    merged section is absent (e.g. partial / intermediate ELFs).
    """
    prefix = f".{section_name}."

    with elf_path.open("rb") as f:
        try:
            elf = ELFFile(f)
        except ELFError as exc:
            sys.exit(f"ERROR: {elf_path} is not a valid ELF file: {exc}")

        # Primary: merged section present in final ELF
        section = elf.get_section_by_name(section_name)
        if section is not None:
            size = section["sh_size"]
            print(f"  {elf_path.name}: '{section_name}' = {size} B  "
                  f"(merged section)")
            return size

        # Fallback: sum subsections (should not happen for zephyr.elf)
        total = 0
        matched = []
        for sec in elf.iter_sections():
            if sec.name.startswith(prefix) and sec["sh_size"] > 0:
                matched.append(f"{sec.name}({sec['sh_size']}B)")
                total += sec["sh_size"]

        if matched:
            print(f"  {elf_path.name}: subsections {', '.join(matched)} "
                  f"= {total} B  (fallback)")
        else:
            print(f"  {elf_path.name}: section '{section_name}' not found "
                  f"— assuming 0 items")

        return total


def generate_header(out_path: pathlib.Path, section_name: str,
                    item_type: str, total_bytes: int) -> None:
    # Strip extensions twice to handle .h.tmp → net_if_count.h → net_if_count
    stem  = pathlib.Path(out_path.stem).stem.upper().replace("-", "_").replace(".", "_")
    guard = stem + "_H"
    macro = stem      # e.g. NET_IF_COUNT

    if total_bytes == 0:
        # No ELF yet (first build), caller's initial header already has the
        # sentinel value 1U; we emit 1U here too so the .tmp comparison works
        # correctly and triggers a rebuild on the next run when the real ELF
        # is available.
        count_expr = "1U"
    else:
        # Real count from zephyr.elf, keep sizeof so the expression stays
        # correct if the struct size changes without needing cmake updates.
        count_expr = f"({total_bytes}U / sizeof({item_type}))"

    new_content = (
        f"/* AUTO-GENERATED by gen_section_count.py, do not edit */\n"
        f"#ifndef {guard}\n"
        f"#define {guard}\n"
        f"\n"
        f"/** Number of items in the '{section_name}' linker section. */\n"
        f"#define {macro}  {count_expr}\n"
        f"\n"
        f"#endif /* {guard} */\n"
    )

    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(new_content)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Extract iterable-section count from zephyr.elf → C header")
    parser.add_argument("--section", required=True,
                        help="Section name, e.g. net_if_area")
    parser.add_argument("--type", required=True, dest="item_type",
                        help="C type for sizeof(), e.g. 'struct net_if'")
    parser.add_argument("--output", required=True,
                        help="Path for the generated header")
    parser.add_argument("elf", help="Path to the fully-linked zephyr.elf")
    args = parser.parse_args()

    elf_path = pathlib.Path(args.elf)
    out_path = pathlib.Path(args.output)

    if not elf_path.is_file():
        # First build, no ELF yet, emit sentinel count = 1 and continue.
        print(f"[gen_section_count] '{args.section}': "
              f"zephyr.elf not found, using sentinel count = 1")
        total_bytes = 0
    else:
        total_bytes = get_merged_section_size(elf_path, args.section)

    generate_header(out_path, args.section, args.item_type, total_bytes)
    print(f"[gen_section_count] '{args.section}': "
          f"{total_bytes} B → {out_path}")


if __name__ == "__main__":
    main()
