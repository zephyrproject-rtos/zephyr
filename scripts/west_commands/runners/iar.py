"""Runner for debugging through IAR Embedded Workbench and flashing with CSpyBat."""

import argparse
import json
import os
import sys
from pathlib import Path
from subprocess import CalledProcessError

from runners.core import RunnerCaps, RunnerConfig, ZephyrBinaryRunner


class IarBinaryRunner(ZephyrBinaryRunner):
    """Runner front-end for EW"""

    def __init__(
        self,
        cfg: RunnerConfig,
        device: str,
        driver: str,
        ewarm: Path | None,
        file: str | None,
        run_to_main: bool,
    ) -> None:
        super().__init__(cfg)

        self._device = device
        self._driver = driver
        self._ewarm = ewarm
        self._file = file
        self._run_to_main = run_to_main

    def _get_iar_toolchain_path(self) -> Path:
        """Obtain the path to an IAR ARM toolchain path. For an EWARM installation,
        this would mean <path-to-EWARM>/arm
        """

        # Set the IAR ARM toolchain path from --ewarm option if set and valid
        if self._ewarm:
            iar_toolchain_path = Path(self._ewarm) / "arm"
            if iar_toolchain_path.exists():
                return iar_toolchain_path
            else:
                raise NotImplementedError(
                    f"provided path to EWARM installation {self._ewarm} does not exist."
                )

        # Set the toolchain path to IAR_TOOLCHAIN_PATH environment variable if set and valid.
        iar_toolchain_path_env = os.getenv("IAR_TOOLCHAIN_PATH")
        if iar_toolchain_path_env is not None:
            iar_toolchain_path = Path(iar_toolchain_path_env)

            if iar_toolchain_path.exists():
                return iar_toolchain_path

        raise NotImplementedError(
            "Could not find a valid IAR embedded workbench for ARM installation."
            " You can either supply a path through --ewarm=<path-to-EWARM-installation>,"
            " or you can set the IAR_TOOLCHAIN_PATH environment variable"
            " (https://docs.zephyrproject.org/latest/develop/toolchains/iar_arm_toolchain.html)"
        )

    def _get_iaride_path(self, iar_toolchain_path) -> str:
        iar_ew_dir = iar_toolchain_path.parent  # get main EW parent dir
        iaride_path = iar_ew_dir / "common" / "bin" / "iaride"

        return str(iaride_path)

    def _get_cspybat_path(self, iar_toolchain_path) -> str:
        iar_ew_dir = iar_toolchain_path.parent  # get main EW parent dir
        cspybat_path = iar_ew_dir / "common" / "bin" / "CSpyBat"

        return str(cspybat_path)

    def _generate_launch_file(self, command) -> str:
        # Add launch file base
        launch_options: dict[str, str | bool] = {
            "name": f"Generated by west {command}",
            "type": "cspy",
            "target": "arm",
        }

        launch_options["device"] = self._device

        launch_options["driver"] = self._driver

        if self._file is None:
            dl_file = self.cfg.elf_file
        else:
            dl_file = self._file

        if dl_file is None:
            raise FileNotFoundError("Could not find program")

        launch_options["program"] = dl_file

        launch_options["request"] = "launch"

        if command == "debug" and self._run_to_main:
            launch_options["stopOnSymbol"] = "main"

        if command == "flash":
            launch_options["leaveTargetRunning"] = True

        launch_file_path = Path(dl_file).parents[1] / "launch.json"

        with open(launch_file_path, "w") as f:
            json.dump({"configurations": [launch_options]}, f, indent=2)

        return str(launch_file_path)

    @classmethod
    def name(cls):
        return "iar"

    @classmethod
    def capabilities(cls):
        return RunnerCaps(commands={"debug", "flash"}, file=True, hide_load_files=True)

    @classmethod
    def do_add_parser(cls, parser):
        ## Required arguments ##
        parser.add_argument(
            "--device",
            type=str,
            required=True,
            help="Device name",
        )

        ## Optional arguments ##
        parser.add_argument(
            "--driver",
            type=str,
            required=False,
            default="I-jet",
            help="Driver name, default I-jet",
        )
        parser.add_argument(
            "--ewarm",
            type=Path,
            required=False,
            help="Path to EWARM installation",
        )
        parser.add_argument(
            "--run-to-main",
            action="store_true",
            required=False,
            help="Run the program and break at main",
        )

    @classmethod
    def do_create(cls, cfg: RunnerConfig, args: argparse.Namespace) -> "IarBinaryRunner":
        return IarBinaryRunner(
            cfg,
            device=args.device,
            driver=args.driver,
            ewarm=args.ewarm,
            file=args.file,
            run_to_main=args.run_to_main,
        )

    def do_run(self, command: str, **kwargs):
        try:
            match command:
                case "debug":
                    self.debug(**kwargs)
                case "flash":
                    self.flash(**kwargs)
        except (CalledProcessError, FileNotFoundError, NotImplementedError) as e:
            self.logger.error(f"Error during {command} command: {e}")
            sys.exit(1)

    def debug(self, **kwargs) -> None:
        # Set the IAR ARM toolchain path
        iar_toolchain_path = self._get_iar_toolchain_path()

        launch_file_path = self._generate_launch_file("debug")

        iaride_exe_path = self._get_iaride_path(iar_toolchain_path)

        cmd = [iaride_exe_path, "--launch", "--launch_file", launch_file_path]

        self.check_call(cmd)

    def flash(self, **kwargs) -> None:
        # Set the IAR ARM toolchain path
        iar_toolchain_path = self._get_iar_toolchain_path()

        cspybat_exe_path = self._get_cspybat_path(iar_toolchain_path)

        launch_file_path = self._generate_launch_file("flash")

        cmd = [cspybat_exe_path, "--launch-file", launch_file_path, "--timeout=0"]

        self.check_call(cmd)
