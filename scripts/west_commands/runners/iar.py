"""Runner for debugging through IAR Embedded Workbench and flashing with CSpyBat."""

import argparse
import json
import os
import sys
from pathlib import Path
from subprocess import CalledProcessError

from runners.core import RunnerCaps, RunnerConfig, ZephyrBinaryRunner


class IarBinaryRunner(ZephyrBinaryRunner):
    """Runner front-end for EW"""

    def __init__(
        self,
        cfg: RunnerConfig,
        device: str,
        driver: str,
        ewarm: Path | None,
        file: str | None,
        generate_only: bool,
        stop_on_symbol: str,
    ) -> None:
        super().__init__(cfg)

        self._device = device
        self._driver = driver
        self._ewarm = ewarm
        self._file = file
        self._generate_only = generate_only
        self._stop_on_symbol = stop_on_symbol

    def _get_iar_toolchain_path(self) -> Path:
        """Obtain the path to an IAR ARM toolchain path. For an EWARM installation,
        this would mean <path-to-EWARM>/arm
        """

        # Set the IAR ARM toolchain path from --ewarm option if set and valid
        if self._ewarm:
            iar_toolchain_path = self._ewarm / "arm"
            if iar_toolchain_path.exists():
                return iar_toolchain_path
            else:
                raise NotImplementedError(
                    f"provided path to EWARM installation {self._ewarm} does not exist."
                )

        # Set the toolchain path to IAR_TOOLCHAIN_PATH environment variable if set and valid.
        iar_toolchain_path_env = os.getenv("IAR_TOOLCHAIN_PATH")
        if iar_toolchain_path_env is not None:
            iar_toolchain_path = Path(iar_toolchain_path_env)

            if iar_toolchain_path.exists():
                return iar_toolchain_path

        raise NotImplementedError(
            "Could not find a valid IAR embedded workbench for ARM installation."
            " You can either supply a path through --ewarm=<path-to-EWARM-installation>,"
            " or you can set the IAR_TOOLCHAIN_PATH environment variable"
            " (https://docs.zephyrproject.org/latest/develop/toolchains/iar_arm_toolchain.html)"
        )

    def _get_iar_tool_path(self, iar_toolchain_path: Path, tool: str) -> str:
        iar_ew_dir = iar_toolchain_path.parent  # get main EW parent dir
        iar_tool_path = iar_ew_dir / "common" / "bin" / tool

        return str(iar_tool_path)

    def _generate_launch_file(self, command: str) -> str:
        iar_toolchain_path = self._get_iar_toolchain_path()

        iarbuild_path = self._get_iar_tool_path(iar_toolchain_path, "iarbuild")

        # Create launch file base
        cmd = [iarbuild_path, "-E", "create_cspy_launch"]

        cmd.append("--driver")
        cmd.append(self._driver)
        cmd.append("--device")
        cmd.append(self._device)

        out = self.check_output(cmd)
        parsed_out = json.loads(out)

        config = parsed_out["configurations"][0]

        config["name"] = f"Generated by west {command}"

        # Enable Zephyr plugin
        if command == "debug":
            config["plugins"] = ["$TOOLKIT_DIR$/plugins/rtos/Zephyr/ZephyrArmPlugin.dll"]

        if self._file is None:
            dl_file = self.cfg.elf_file
        else:
            dl_file = self._file

        if dl_file is None:
            raise FileNotFoundError("Could not find program")

        config["program"] = str(Path(dl_file).resolve())

        # Use symbol "_vector_table" to locate vector table
        new_driver_options = {"--drv_vector_table_base": "=_vector_table"}

        if self._driver in ["I-jet", "CMSIS-DAP"]:
            # Always use reset method with bootloader when available
            bootloader_reset = next(
                (
                    entry
                    for entry in config["driverOptions"]
                    if entry.startswith("--reset_style=\"7,")
                ),
                False,
            )
            if bootloader_reset:
                new_driver_options["--jet_standard_reset"] = "=7,0,0"

        for option in new_driver_options:
            # Remove option if already set
            existing_option = next(
                (entry for entry in config["driverOptions"] if entry.startswith(option)), None
            )
            if existing_option:
                config["driverOptions"].remove(existing_option)
            config["driverOptions"].append(option + new_driver_options[option])

        # Overwrite default "main"
        config["stopOnSymbol"] = self._stop_on_symbol

        if command == "flash":
            config["leaveTargetRunning"] = True

        launch_file_path = Path(self.cfg.build_dir) / "launch.json"

        with open(launch_file_path, "w") as f:
            json.dump(parsed_out, f, indent=2)

        self.logger.info(f"IAR launch file written to: {launch_file_path}")

        return str(launch_file_path)

    @classmethod
    def name(cls):
        return "iar"

    @classmethod
    def capabilities(cls):
        return RunnerCaps(commands={"debug", "flash"}, file=True, hide_load_files=True)

    @classmethod
    def do_add_parser(cls, parser):
        ## Required arguments ##
        parser.add_argument(
            "--device",
            type=str,
            required=True,
            help="Device name",
        )

        ## Optional arguments ##
        parser.add_argument(
            "--driver",
            type=str,
            required=False,
            default="I-jet",
            help="Driver name, default I-jet",
        )
        parser.add_argument(
            "--ewarm",
            type=Path,
            required=False,
            help="Path to EWARM installation",
        )
        parser.add_argument(
            "--generate-only",
            "-g",
            action="store_true",
            required=False,
            help="Only generate launch.json. Do not start flashing/debugging",
        )
        parser.add_argument(
            "--stop-on-symbol",
            type=str,
            required=False,
            default="",
            help="Run the program and break at the given symbol",
        )

    @classmethod
    def do_create(cls, cfg: RunnerConfig, args: argparse.Namespace) -> "IarBinaryRunner":
        return IarBinaryRunner(
            cfg,
            device=args.device,
            driver=args.driver,
            ewarm=args.ewarm,
            file=args.file,
            generate_only=args.generate_only,
            stop_on_symbol=args.stop_on_symbol,
        )

    def do_run(self, command: str, **kwargs):
        try:
            match command:
                case "debug":
                    self.debug(**kwargs)
                case "flash":
                    self.flash(**kwargs)
        except (CalledProcessError, FileNotFoundError, NotImplementedError) as e:
            self.logger.error(f"Error during {command} command: {e}")
            sys.exit(1)

    def debug(self, **kwargs) -> None:
        # Set the IAR ARM toolchain path
        iar_toolchain_path = self._get_iar_toolchain_path()

        launch_file_path = self._generate_launch_file("debug")

        if self._generate_only:
            self.logger.info("generate only: not launching iaride")
            return

        iaride_exe_path = self._get_iar_tool_path(iar_toolchain_path, "iaride")

        cmd = [iaride_exe_path, "--launch", "--launch_file", launch_file_path]

        self.check_call(cmd)

    def flash(self, **kwargs) -> None:
        # Set the IAR ARM toolchain path
        iar_toolchain_path = self._get_iar_toolchain_path()

        launch_file_path = self._generate_launch_file("flash")

        if self._generate_only:
            self.logger.info("generate only: not launching CSpyBat")
            return

        cspybat_exe_path = self._get_iar_tool_path(iar_toolchain_path, "CSpyBat")

        cmd = [cspybat_exe_path, "--launch-file", launch_file_path, "--timeout=0"]

        self.check_call(cmd)
