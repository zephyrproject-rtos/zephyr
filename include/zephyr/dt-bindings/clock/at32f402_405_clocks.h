#define AT32_CLOCK_DMA1       AT32_CLOCK_CONFIG(AHB1EN, 22)
#define AT32_CLOCK_DMA2       AT32_CLOCK_CONFIG(AHB1EN, 24)
#define AT32_CLOCK_USBHS      AT32_CLOCK_CONFIG(AHB1EN, 29)

/* AHB2 peripherals */
