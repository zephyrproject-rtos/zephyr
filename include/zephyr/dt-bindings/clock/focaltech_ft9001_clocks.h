/**
 * @defgroup focaltech_ft9001_clocks FocalTech FT9001 clock identifiers
 * @brief Clock identifiers for FocalTech FT9001
 * @ingroup devicetree-clocks
 *
 * Clock identifiers follow the naming convention:
 * @c FOCALTECH_CLOCK_\<PERIPHERAL\>
 *
 * For example, @c FOCALTECH_CLOCK_SPI1 identifies the clock gate for @c SPI1.
 *
 * Valid peripherals (grouped by clock gate control register):
 *
 * - **MULTICGCR**: @c OTP_AHB, @c MCC, @c MCCADR, @c ADC, @c MESH, @c TC,
 *   @c CLKOUT, @c KEY_CTRL, @c CCM_RESET, @c OTP_IPS, @c CPM_IPS, @c TRACE,
 *   @c I2S_M, @c I2S_S, @c CRC0, @c CRC1
 * - **SYSCGCR**: @c SSISLV1, @c DMAC1, @c DMAC2, @c AHB2_MUX, @c PXLP1_AHB,
 *   @c PXLP2_AHB, @c SRAMD, @c SRAM0, @c SRAM1, @c SRAM2, @c SRAM3_4,
 *   @c SSI4, @c SSI5, @c SSI6
 * - **AHB3CGCR**: @c ROM, @c ROM_OTP_MUX, @c AHB3_MUX, @c USBC,
 *   @c PXLP1_APB, @c PXLP2_APB, @c I2S1, @c I2S2
 * - **ARITHCGCR**: @c AES, @c SM4, @c RF_AES_SM4, @c CRYPTO, @c SHA,
 *   @c EDMAC0, @c DES, @c ZUC, @c AHB2MLB, @c AHB2IPS2, @c TRNG
 * - **IPSCGCR**: @c IO_CTRL, @c WDT, @c RTC, @c PIT1, @c PIT2, @c USI1,
 *   @c EDMAC1, @c SPI1, @c SPI2, @c SPI3, @c SCI1, @c SCI2, @c USI2,
 *   @c CAN1, @c I2C1, @c PWM0, @c I2C2, @c CAN2, @c SCI3, @c QADC, @c DAC,
 *   @c MCC_IPS, @c PWMT1, @c LD, @c PWMT2, @c PGD, @c SEC_DET, @c PCI,
 *   @c PMURTC, @c AHB2IPS, @c PWMT3
 * - **EPORTCGCR**: @c EPORT0, @c EPORT1, @c EPORT2, @c EPORT3, @c EPORT4,
 *   @c EPORT5, @c EPORT6, @c EPORT7, @c ACMP1, @c ACMP2
 *
 * @{
 */

/** @cond INTERNAL_HIDDEN */

#define FOCALTECH_CLOCK_SHIFT 6U
#define FOCALTECH_CLOCK_MASK  ((1U << FOCALTECH_CLOCK_SHIFT) - 1U)

#define FOCALTECH_CLOCK_CONFIG(reg, bit)                                                           \
	(((FOCALTECH_##reg##_OFFSET) << FOCALTECH_CLOCK_SHIFT) | ((bit) & FOCALTECH_CLOCK_MASK))

